package com.android.pet.view;

public class ChildBean {

	String mChildName;
	int mChildImage;

	public ChildBean(String mChildName, int mChildImage) {
		super();
		this.mChildName = mChildName;
		this.mChildImage = mChildImage;
	}

	public String getmChildName() {
		return mChildName;
	}

	public void setmChildName(String mChildName) {
		this.mChildName = mChildName;
	}

	public int getmChildImage() {
		return mChildImage;
	}

	public void setmChildImage(int mChildImage) {
		this.mChildImage = mChildImage;
	}

}
